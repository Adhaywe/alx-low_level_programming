#include <stdio.h>
#include "main.h"

/**
 * main - Entry point
 * Description: A function that checks for uppercase character
 * Return: 1 if c is uppercase, 0 otherwise
 */
int main(void)
{
	int _isupper(int c);
}
int _isupper(int c)
{
	if (isupper(c))
	{
		return (1);
	}
	else
	{
		return (0);
	}
	return (0);
}
